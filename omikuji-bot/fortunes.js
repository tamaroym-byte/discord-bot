// ===== ランダム =====
function seededRandom(seed) {
  const x = Math.sin(seed) * 10000;
  return x - Math.floor(x);
}

function weightedPick(items, offset, seed) {
  const total = items.reduce((s, i) => s + i.weight, 0);
  const r = seededRandom(seed + offset) * total;

  let acc = 0;
  for (const i of items) {
    acc += i.weight;
    if (r < acc) return i.name;
  }
}

function pickFromLuck(table, luck, offset, seed) {
  const arr = table[luck];
  return arr[Math.floor(seededRandom(seed + offset) * arr.length)];
}

function pickDirection(seed, offset) {
  return directions[Math.floor(seededRandom(seed + offset) * directions.length)];
}

function buildTravel(luck, seed) {
  const dir = pickDirection(seed, 100);
  const template = pickFromLuck(travelTemplates, luck, 101, seed);
  return template.replace("{dir}", dir);
}

// ===== 本体 =====
function getDailyFortune(userId, redraw = false) {
  const today = new Date().toISOString().slice(0, 10).replace(/-/g, "");
  const base = Number(userId.slice(-6)) + Number(today);
  const seed = redraw ? base + 9999 : base;

  const luck = weightedPick(luckLevels, 0, seed);

  return {
    luck,
    luckMessage: pickFromLuck(luckMessages, luck, 1, seed),
    wish: pickFromLuck(wishes, luck, 2, seed),
    person: pickFromLuck(people, luck, 3, seed),
    lost: pickFromLuck(losts, luck, 4, seed),
    travel: buildTravel(luck, seed)
  };
}

